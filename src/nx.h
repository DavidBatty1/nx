//----------------------------------------------------------------------------------------------------------------------
// NX system
// Launches other subsystems based command line arguments, and manages memory.
//----------------------------------------------------------------------------------------------------------------------

#pragma once

#include "machine.h"

typedef struct
{
    Platform    platform;
}
NxConfig;

typedef struct
{
    NxConfig    config;
    Machine     machine;
}
Nx;

//----------------------------------------------------------------------------------------------------------------------
// API to platform layer
//----------------------------------------------------------------------------------------------------------------------

// Initialise the NX system by providing callback functions for platform-specific memory and IO operations.
bool nxOpen(Nx* N, NxConfig* config);

// Close down the NX system
void nxClose(Nx* N);

//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------

#ifdef NX_IMPL

//----------------------------------------------------------------------------------------------------------------------
// Public API
//----------------------------------------------------------------------------------------------------------------------

bool nxOpen(Nx* N, NxConfig* config)
{
    ASSERT(N);
    ASSERT(config);

    N->config = *config;
    MachineConfig machineConfig;
    machineConfig.platform = config->platform;
    return machineOpen(&N->machine, &machineConfig);
}

void nxClose(Nx* N)
{
    ASSERT(N);
    machineClose(&N->machine);
}

//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------

#endif // NX_IMPL
